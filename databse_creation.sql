CREATE TABLE sales(
   restaurant_name VARCHAR(300),
   restaurant_type VARCHAR(30),
   rating VARCHAR(5),
   num_of_ratings INTEGER,
   avg_cost_two_people VARCHAR(5), 
   online_order VARCHAR(5),
   table_booking VARCHAR(5),
   cuisines_type VARCHAR(83),
   area VARCHAR(30),
   local_address VARCHAR(30) 
);
